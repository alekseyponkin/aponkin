package ru.job4j.generic;

/**
 * Abstract class Base.
 *
 * @author Ponkin Aleksey
 * @version 1.0.0
 * @since 20.11.2017
 */
public abstract class Base {
    /**
     * Get Id.
     * @return id.
     */
    abstract String getId();

    /**
     * Set Id.
     * @param id for set.
     */
    abstract void setId(String id);
}